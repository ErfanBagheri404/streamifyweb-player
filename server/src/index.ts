function mapFilterToInvidiousType(filter: string): string | undefined {
  const lower = filter.toLowerCase();
  if (lower === "video" || lower === "videos") return "video";
  if (lower === "playlist" || lower === "playlists") return "playlist";
  if (lower === "channel" || lower === "channels") return "channel";
  return undefined;
}

async function searchInvidious(
  query: string,
  filter: string,
  page: number,
  limit: number
) {
  console.log(
    `[searchInvidious] Query: "${query}", Filter: "${filter}", Page: ${page}, Limit: ${limit}`
  );
  const effectiveLimit = Math.min(limit, 20);
  const typeParam = mapFilterToInvidiousType(filter);

  const instances = [
    "https://yt.chocolatemoo53.com",
    "https://inv.thepixora.com",
    "https://yt.omada.cafe",
    "https://invidious.snopyta.org",
    "https://yewtu.be",
    "https://inv.riverside.rocks",
  ];

  for (const instance of instances) {
    try {
      const url = new URL(`${instance}/api/v1/search`);
      url.searchParams.set("q", query);
      url.searchParams.set("page", String(page));
      if (typeParam) url.searchParams.set("type", typeParam);

      console.log(`[searchInvidious] Trying ${url.toString()}`);
      const res = await fetch(url.toString(), {
        headers: { "User-Agent": USER_AGENT },
      });
      console.log(
        `[searchInvidious] Response status: ${res.status} from ${instance}`
      );

      if (res.ok) {
        let data = (await res.json()) as InvidiousSearchItem[];
        if (!Array.isArray(data)) continue;

        // Standardize: fix thumbnail URLs and qualities
        const standardized = data.map((item) =>
          rewriteAndStandardizeItem(item, instance)
        );

        // Normalize each item: extract a direct thumbnailUrl and set source to "youtube"
        const items = standardized.slice(0, effectiveLimit).map((item) => {
          let thumbnailUrl = "";

          // Extract best thumbnail for videos
          if (item.type === "video" && Array.isArray(item.videoThumbnails)) {
            // Prefer highest quality (maxres > sddefault > hqdefault etc.)
            const sorted = [...item.videoThumbnails].sort(
              (a, b) => b.width - a.width
            );
            thumbnailUrl = sorted[0]?.url || "";
          }
          // Playlist thumbnail
          else if (item.type === "playlist" && item.playlistThumbnail) {
            thumbnailUrl = item.playlistThumbnail;
          }
          // Channel avatar
          else if (
            item.type === "channel" &&
            Array.isArray(item.authorThumbnails)
          ) {
            thumbnailUrl = item.authorThumbnails[0]?.url || "";
          }

          // Return a clean object that matches the frontend's expectations
          return {
            ...item,
            thumbnailUrl,
            source: "youtube", // Forces 128×72 / 48×48 layout in ResultItem
          };
        });

        const hasMore = data.length >= effectiveLimit;
        const nextpage = hasMore ? String(page + 1) : null;
        console.log(
          `[searchInvidious] Success from ${instance}: ${items.length} items (normalized with thumbnailUrl)`
        );
        return { items, nextpage };
      } else {
        const text = await res.text();
        console.error(`[searchInvidious] Failed ${instance}: ${res.status}`);
      }
    } catch (err) {
      console.error(`[searchInvidious] Error with ${instance}:`, err);
    }
  }
  return { items: [], nextpage: null };
}

