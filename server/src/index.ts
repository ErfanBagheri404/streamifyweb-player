function rewriteAndStandardizeItem(item: any, instanceUrl: string): any {
  if (!item || typeof item !== "object") return item;

  const result = { ...item };

  // For channel items, add a 'name' field (alias for 'author') for frontend compatibility
  if (result.type === "channel" && result.author && !result.name) {
    result.name = result.author;
  }

  // Process videoThumbnails
  if (Array.isArray(result.videoThumbnails)) {
    // Define standard qualities we want to output (in order of preference)
    const qualityMap: Record<
      string,
      { quality: string; width: number; height: number }
    > = {
      maxres: { quality: "maxres", width: 1280, height: 720 },
      maxresdefault: { quality: "maxres", width: 1280, height: 720 },
      sddefault: { quality: "sddefault", width: 640, height: 480 },
      hqdefault: { quality: "high", width: 480, height: 360 },
      mqdefault: { quality: "medium", width: 320, height: 180 },
      default: { quality: "default", width: 120, height: 90 },
    };

    // Map and filter to only standard qualities, rewriting URLs
    const standardized = result.videoThumbnails
      .map((thumb: any) => {
        let qualityKey = thumb.quality || "";
        if (thumb.url.includes("maxres")) qualityKey = "maxres";
        else if (thumb.url.includes("sddefault")) qualityKey = "sddefault";
        else if (thumb.url.includes("hqdefault")) qualityKey = "hqdefault";
        else if (thumb.url.includes("mqdefault")) qualityKey = "mqdefault";
        else if (thumb.url.includes("default")) qualityKey = "default";

        const mapped = qualityMap[qualityKey];
        if (!mapped) return null;

        // Rewrite URL to use the instance domain
        const urlPath = new URL(thumb.url, instanceUrl).pathname;
        return {
          quality: mapped.quality,
          url: `${instanceUrl}${urlPath}`,
          width: mapped.width,
          height: mapped.height,
        };
      })
      .filter(Boolean);

    // Remove duplicates (keep first occurrence of each quality)
    const seen = new Set();
    result.videoThumbnails = standardized.filter((t: any) => {
      if (seen.has(t.quality)) return false;
      seen.add(t.quality);
      return true;
    });
  }

  // Process authorThumbnails
  if (Array.isArray(result.authorThumbnails)) {
    result.authorThumbnails = result.authorThumbnails.map((thumb: any) => {
      // Parse the original URL relative to the instance
      let pathname = new URL(thumb.url, instanceUrl).pathname;

      // If the instance didn't add the /ggpht/ proxy prefix, add it now
      if (!pathname.startsWith("/ggpht/") && pathname.startsWith("/ytc/")) {
        pathname = "/ggpht" + pathname; // → /ggpht/ytc/...
      }

      // Strip the -mo suffix if present (Music channel avatars)
      if (pathname.endsWith("-mo")) {
        pathname = pathname.slice(0, -3);
      }

      return {
        ...thumb,
        url: `${instanceUrl}${pathname}`,
      };
    });
  }

  // Also rewrite any standalone thumbnail URL fields
  if (
    result.playlistThumbnail &&
    typeof result.playlistThumbnail === "string"
  ) {
    const urlPath = new URL(result.playlistThumbnail, instanceUrl).pathname;
    result.playlistThumbnail = `${instanceUrl}${urlPath}`;
  }

  return result;
}

// ---------- PIPED (YouTube) – unchanged ----------
const fetchFromPiped = async (
  endpoint: string
): Promise<Record<string, unknown> | null> => {
  console.log(`[fetchFromPiped] Starting fetch for endpoint: ${endpoint}`);
  for (const baseUrl of PIPED_INSTANCES) {
    try {
      const url = `${baseUrl}${endpoint}`;
      console.log(`[fetchFromPiped] Trying URL: ${url}`);
      const res = await fetch(url, {
        headers: { "User-Agent": USER_AGENT },
      });
      console.log(`[fetchFromPiped] Response status: ${res.status}`);
      if (res.ok) {
        const json: unknown = await res.json();
        console.log(
          `[fetchFromPiped] JSON parsed, keys: ${Object.keys(
            json as object
          ).join(", ")}`
        );
        const itemCount = (json as HasItems).items?.length ?? 0;
        console.log(`[fetchFromPiped] Item count: ${itemCount}`);
        if (json && typeof json === "object")
          return json as Record<string, unknown>;
      } else {
        const text = await res.text();
        console.error(`[fetchFromPiped] Non-OK response body: ${text}`);
      }
    } catch (err) {
      console.error(`[fetchFromPiped] Fetch failed for ${baseUrl}:`, err);
      continue;
    }
  }
  console.error("[fetchFromPiped] All Piped instances failed");
  return null;
};

async function searchYouTube(
  query: string,
  filter: string,
  _page: number,
  _limit: number,
  nextpage?: string
) {
  console.log(
    `[searchYouTube] Query: "${query}", Filter: "${filter}", Nextpage: ${nextpage}`
  );
  const filterParam = filter === "" ? "all" : filter;
  let endpoint = "";
  if (nextpage) {
    endpoint = `/nextpage/search?nextpage=${encodeURIComponent(nextpage)}`;
  } else {
    endpoint = `/search?q=${encodeURIComponent(query)}&filter=${filterParam}`;
  }
  console.log(`[searchYouTube] Piped endpoint: ${endpoint}`);
  const data = await fetchFromPiped(endpoint);
  console.log(
    `[searchYouTube] fetchFromPiped returned: ${data ? "object" : "null"}`
  );

  const rawItems: RawPipedItem[] = (data?.items as RawPipedItem[]) ?? [];
  const newNextpage = data?.nextpage ?? null;
  console.log(`[searchYouTube] Items found: ${rawItems.length}`);

  const results = rawItems.map((item: RawPipedItem) => {
    const videoThumbnails = Array.isArray(item.videoThumbnails)
      ? (item.videoThumbnails as Array<{
          width?: number;
          height?: number;
          quality?: string;
          url?: string;
        }>)
      : undefined;
    const authorThumbnails = Array.isArray(item.authorThumbnails)
      ? (item.authorThumbnails as Array<{
          width?: number;
          height?: number;
          quality?: string;
          url?: string;
        }>)
      : undefined;

    return {
      ...item,
      thumbnailUrl:
        pickBestThumbnailUrl(videoThumbnails) ||
        pickBestThumbnailUrl(authorThumbnails) ||
        (typeof item.playlistThumbnail === "string" ? item.playlistThumbnail : "") ||
        (typeof item.thumbnailUrl === "string" ? item.thumbnailUrl : "") ||
        (typeof item.thumbnail === "string" ? item.thumbnail : ""),
      source: "youtube" as const,
    };
  });

  console.log(`[searchYouTube] Final results count: ${results.length}`);
  return { items: results, nextpage: newNextpage };
}

